def main():
    moves = build_moves()

    # Remove exact duplicates that might have been created with _gen# suffixes
    # when the base version already exists
    # Actually, let's just count unique IDs
    print(f"Total move entries generated: {len(moves)}")

    output_path = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), "data", "moves.json")
    os.makedirs(os.path.dirname(output_path), exist_ok=True)

    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(moves, f, indent=2, ensure_ascii=False)

    print(f"Moves written to: {output_path}")

    # Stats
    types_count = {}
    categories = {"physical": 0, "special": 0, "status": 0}
    flag_counts = {}
    effect_counts = {}
    priority_moves = []

    for mid, mv in moves.items():
        t = mv["type"]
        types_count[t] = types_count.get(t, 0) + 1
        categories[mv["category"]] += 1
        for fl in mv["flags"]:
            flag_counts[fl] = flag_counts.get(fl, 0) + 1
        if mv["effect"]:
            effect_counts[mv["effect"]] = effect_counts.get(mv["effect"], 0) + 1
        if mv["priority"] != 0:
            priority_moves.append((mid, mv["name"], mv["priority"]))

    print(f"\n--- Stats ---")
    print(f"By type:")
    for t in sorted(types_count, key=types_count.get, reverse=True):
        print(f"  {t}: {types_count[t]}")
    print(f"\nBy category:")
    for c, n in categories.items():
        print(f"  {c}: {n}")
    print(f"\nFlag counts:")
    for fl in sorted(flag_counts, key=flag_counts.get, reverse=True):
        print(f"  {fl}: {flag_counts[fl]}")
    print(f"\nPriority moves ({len(priority_moves)}):")
    for mid, name, pri in sorted(priority_moves, key=lambda x: -x[2]):
        print(f"  {name}: {pri:+d}")


if __name__ == "__main__":
    main()
